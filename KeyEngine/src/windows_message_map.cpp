std::string WindowsMessageMap::toString( const DWORD uMsg ) const noexcept
{
	const auto it = m_map.find( uMsg );
	std::ostringstream oss;
	if ( it != m_map.end() )
	{
		oss << it->second;
	}
	else
	{
		oss << uMsg << "-UNKNOWN_WINDOWS_MESSAGE_ID";
	}

	return oss.str();
}